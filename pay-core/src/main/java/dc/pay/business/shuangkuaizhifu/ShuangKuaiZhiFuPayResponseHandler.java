    //sign    签名，将以上参数（不包括sign本身）按字典排序后拼接上商户秘钥然后用md5加密。例如md5(sid=10005&price=200...&notifyUrl=http://商户域名/notifyurl商户秘钥)。（商户秘钥可在“对接信息”页面查看）
