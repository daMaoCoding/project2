    protected String buildPaySign(Map<String,String> api_response_params) throws PayException {
        //1、参数列表中，除去signature外，其他所有非空的参数都要参与签名，值为空的参数不用参与签名。
        //2、签名顺序按照参数名a到z的顺序排序，若遇到相同的首字母，则看第二个字母，以此类推，组成规则如下：
        StringBuffer signSrc= new StringBuffer();
        signSrc.append(cpOrderId+"=").append(api_response_params.get(cpOrderId)).append("|");
        signSrc.append(cpId+"=").append(api_response_params.get(cpId)).append("|");
        signSrc.append(userId+"=").append(api_response_params.get(userId)).append("|");
        signSrc.append(payType+"=").append(api_response_params.get(payType)).append("|");
        signSrc.append(notifyUrl+"=").append(api_response_params.get(notifyUrl)).append("|");
        signSrc.append(price+"=").append(api_response_params.get(price)).append("|");
        signSrc.append(key+"=").append(channelWrapper.getAPI_KEY());
        //删除最后一个字符
        //signSrc.deleteCharAt(paramsStr.length()-1);
        String paramsStr = signSrc.toString();
        String signMd5 = HandlerUtil.getMD5UpperCase(paramsStr).toLowerCase();
        log.debug("[智联支付]-[请求支付]-2.生成加密URL签名完成：{}" ,JSON.toJSONString(signMd5));
        return signMd5;
    }
    
